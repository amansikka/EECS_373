uint8_t find_direction(int16_t x){

	uint8_t direction = 3;

	if (x > 3000){ // left

		direction = 1;

		return direction;

	}

	else if (x < -3000){ // right
		direction = 2;
		return direction;
	}

	else { // straight
		direction = 3;
		return direction;
	}

}



